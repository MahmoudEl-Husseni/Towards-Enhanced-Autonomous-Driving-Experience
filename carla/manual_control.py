def game_loop(args):
    pygame.init()
    pygame.font.init()
    world = None

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(10.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)
        font = get_font()

        hud = HUD(args.width, args.height)
        world = World(client.get_world(), hud, args)
        # stm = STM(server_IP=('10.182.200.217', 12346),
                #   STM_IP=('10.182.200.225',  12345) , STM_IP2=('10.182.200.225',  12344))
        # print("STM Initialized..")
        # controller = Controller(world , stm)
        controller = KeyboardControl(world, args.autopilot)

        clock = pygame.time.Clock()
        i = 0
        with CarlaSyncMode(world.world, 
                           world.camera_manager, 
                           world.depth_camera, 
                           world.ss_camera, 
                           world.lidar_sensor, 
                           world.imu_sensor, 
                           world.gnss_sensor, 
                           fps=30) as sync_mode:
            while True:
                i += 1
                clock.tick_busy_loop(60)

                if isinstance(controller, Controller):
                    if controller.parse_events(client, world, clock):
                        return
                elif isinstance(controller, KeyboardControl):
                    if controller.parse_events(client, world, clock):
                        return
                
                snapshot, img_raw, depth_raw, ss_raw, lidar_raw, imu_raw, gnss_raw = sync_mode.tick(timeout=2.0)
                # img_raw = CameraManager._parse_image(world.camera_manager, img_raw)
                world.render(display , img_raw)
                fps = round(1.0 / snapshot.timestamp.delta_seconds)
                display.blit(
                    font.render('% 5d FPS (real)' % clock.get_fps(), True, (255, 255, 255)),
                    (8, 10))
                display.blit(
                    font.render('% 5d FPS (simulated)' % fps, True, (255, 255, 255)),
                    (8, 28))

                pygame.display.flip()

    finally:

        if (world and world.recording_enabled):
            client.stop_recorder()

        if world is not None:
            world.destroy()

        pygame.quit()


